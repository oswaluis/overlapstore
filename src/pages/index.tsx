import { GetServerSidePropsContext } from 'next';
import Head from 'next/head'
import {Header} from '../components/Header'
import { TopBar } from '@/components/TopBar';
import { HomeHeroCategories } from '@/components/HomeHeroCategories';
import { Categories } from './models/Categories';
import { AspectRatio, Box, Container, Flex, Text } from '@chakra-ui/react';
import { AdvantageSection } from '@/components/AdvantageSection';
import Image from 'next/image';


type Product={
  id: number;
  title: string;
  price: number;
  description: string;
  category: string;
  image: any;
  rating:{
    count:number;
    rate: number;
  }
}

type Props= {
  products: Product[],
  categories : Categories[]
}

export default function Home({products, categories}: Props) {
  return (
    <>
      <Head>
        <title>Overlap Store</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <TopBar/>
        <Box marginBottom='2rem'>
          <Header/>
        </Box>
        <Container size='lg'>
          <HomeHeroCategories categories={categories}></HomeHeroCategories>
          <AdvantageSection/>
        </Container>

        <Box margin='2rem auto' width='255px' border='solid 1px #000' position='relative'>
          <AspectRatio position='relative' ratio={1} maxWidth='100%'>
            <Image src="https://fakestoreapi.com/img/81fPKd-2AYL._AC_SL1500_.jpg" alt='' fill={true} style={{objectFit:'contain'}}></Image>
          </AspectRatio>
          <Text textAlign='center' margin='1rem'>Fjallraven - Foldsack No. 1 Backpack, Fits 15 Laptops </Text>
          <Text margin='1rem' textAlign='center'>$ 109.95</Text>
        </Box>

        <ol>
          {products.map(product =>{
            return <li key={product.id}>{product.title}</li>
        })}
        </ol>
      </main>
    </>
  )
}

export async function getServerSideProps(context : GetServerSidePropsContext){
  
  const products = await fetch('https://fakestoreapi.com/products')
                        .then(res=>res.json())
  const categories = await fetch('https://fakestoreapi.com/products/categories')
                        .then(res=>res.json())

  return{
    props: {
      products,
      categories
      
    }
    
  }
}
